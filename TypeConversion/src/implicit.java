public class implicit {
    public static void main(String[] args) {
        //implicit casting is the conversion of one data type to another;
        //implicit casting does the conversion automatically and there is no data loss;

        int num = 90;
        float num1 = num; //this is implicit casting from smaller data type to larger datatype

        System.out.println(num);  // in the output you can view there is no data loss;
        System.out.println(num1);
    }
}