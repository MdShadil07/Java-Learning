public class Main {
    public static void main(String[] args) {
        //Arithmatic operators are the mathematical operators which help in performing mathematical calculation
        //  + , - , / , * , %   last one is module funtion which return the remainder of a number Ex: 5%2 will return 1;

        int num1 = 8;
        int num2 = 5;
        int Result1 = num1 + num2;
        System.out.println("The result of Addition Operator: " + Result1);

        int Result2 = num1 - num2;
        System.out.println("The result of Subtraction Operator: " + Result2);

        int Result3 = num1 * num2;
        System.out.println("The result of multiplication Operator: " +Result3);

        int Result4 = num1 % num2;
        System.out.println("The result of the module operator: " + Result4);

        int Result5 = num1 / num2;
        System.out.println("The Result of the Division Operator: " + Result5);
    }
}